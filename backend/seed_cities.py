def seed_entire_iran_cities():
    models.Base.metadata.create_all(bind=engine)
    db = SessionLocal()
    print("seeding cities for all provinces of Iran...")
    
    try:
        db.query(models.City).delete()
        db.commit()
        # جلوگیری از تولید رکوردهای تکراری در صورت اجرای چندباره اسکریپت
        if db.query(models.City).first():
            print("cities are already seeded. skipping seeding process to avoid duplicates.")
            return

        # چرخش روی تک تک استان‌ها و آرایه شهرهای مقابل آن‌ها
        for province_name, cities_list in IRAN_DATA.items():
            
            # ۱. ساخت و ذخیره استان اصلی (parent_id برای استان‌ها همیشه None است)
            province = models.City(title=province_name, parent_id=None)
            db.add(province)
            db.commit()      # کامیت زودهنگام برای اینکه پورت‌گرس شناسه یکتای عددی (id) استان را صادر کند
            db.refresh(province)
            
            # ۲. ساخت گروهی شهرهای فرزند و پیوند دادن آن‌ها با کلید خارجی parent_id استان
            db_cities_batch = []
            for city_name in cities_list:
                city = models.City(title=city_name, parent_id=province.id)
                db_cities_batch.append(city)
                
            db.add_all(db_cities_batch)
            db.commit()
            print(f"processed province: {province_name} with {len(cities_list)} cities.")
            
        print("all cities seeded successfully!")
        
    except Exception as e:
        db.rollback()
        print(f"error occurred while seeding cities: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    seed_entire_iran_cities()